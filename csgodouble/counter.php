function colorReport($color, $limit, $rollTotal, $iRoll) {
	$ii = 1;
	$result = 0;
	$success = 0;
	
	while ($ii <= $rollTotal) {
		if ($color == $iRoll[$ii]) $success++;
		else {
			if ($success == $limit) $result++;
			$success = 0;
		}
		$ii++;
	}
	
	return($result);
}

